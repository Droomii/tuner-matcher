package poly.dto;

public class MainDTO {
	private String near_request;
	private String private_request;
	private String ongoing_deals;
	private String repu;
	
	public String getNear_request() {
		return near_request;
	}
	public void setNear_request(String near_request) {
		this.near_request = near_request;
	}
	public String getPrivate_request() {
		return private_request;
	}
	public void setPrivate_request(String private_request) {
		this.private_request = private_request;
	}
	public String getOngoing_deals() {
		return ongoing_deals;
	}
	public void setOngoing_deals(String ongoing_deals) {
		this.ongoing_deals = ongoing_deals;
	}
	public String getRepu() {
		return repu;
	}
	public void setRepu(String repu) {
		this.repu = repu;
	}
	
	
}
